# program menghitung rata - rata nilai dari sebuah list / daftar

# mendefinisikan list nilai
nilai = [75, 80, 85, 90, 95]

# menghitung jumlah total nilai
total_nilai = sum(nilai)

# menghitung jumlah siswa
jumlah_siswa = len(nilai)

# menghitung rata - rata nilai
rata_rata = total_nilai / jumlah_siswa

# menampilkan hasil
print("jumlah siswa : ", jumlah_siswa)
print("total nilai : ", total_nilai)
print("rata rata : ", rata_rata)

print(nilai[0])