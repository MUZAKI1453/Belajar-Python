# operasi aritmatika

a = 10
b = 5
c = 2
d = 3

# operasi pertambahan
hasil = a + b
print(a, " + ", b, " = ", hasil)

# operasi pengurangan
hasil = a - b
print(a, " - ", b, " = ", hasil)

# operasi perkalian
hasil = a * b
print(a, " * ", b, " = ", hasil)

# operasi pembagian
hasil = a / b
print(a, " / ", b, " = ", hasil)

# operasi eksponen
hasil = a ** c
print(a, " ** ", c, " = ", hasil)

# operasi modulus
hasil = a % d
print(a, " / ", d, " = ", hasil)

# operasi floor division
hasil = a // d
print(a, " // ", d, " = ", hasil)

# prioritas operasi
hasil = a * b - (a / c)
print(hasil)